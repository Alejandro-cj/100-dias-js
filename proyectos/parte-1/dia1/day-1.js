// Declaración de una variable llamada 'nombre' y asignación de un valor.
let nombre = "Juan";

// Estructura condicional (if-else) para verificar si 'nombre' es igual a "Juan".
if (nombre === "Juan") {
  console.log("¡Hola, Juan!"); // Imprime un saludo si la condición es verdadera.
} else {
  console.log("Hola, desconocido."); // Imprime otro mensaje si la condición es falsa.
}

// Bucle (for) para imprimir números del 0 al 4.
for (let i = 0; i < 5; i++) {
  console.log(i);
  // La variable 'i' se incrementa en cada iteración.
}
